std::string json_print(std::string const & s)
{
	using std::cout;
	using std::endl;
	std::stringstream ss;
	int len = s.length();
	for (int i = 0; i < len; ++i) {
		// control characters
		cout << "s["<< i << "]: "
			<< (std::bitset<8>(s[i])) << endl;
		if (i + 1 < len) {
			cout << "s["<< i+1 << "]: "
				<< (std::bitset<8>(s[i+1])) << endl;
			unsigned char c1 = s[i+1] & 0b10111111;
			cout << "c1: " << (std::bitset<8>(c1)) << endl;
		}
		if (i + 2 < len) {
			cout << "s["<< i+2 << "]: "
				<< (std::bitset<8>(s[i+2])) << endl;
			//s[i+2] 
		}
		if (i + 3 < len)
			cout << "s["<< i+3 << "]: "
				<< (std::bitset<8>(s[i+3])) << endl;
		unsigned char ch = s[i] & 0b11011111;
		cout << " bitwise and with 0b11011111 "	<< (ch) << endl;

		if (s[i] >= 0 && s[i] < 32) {
			// control characters
			// some control chars need special handling
			// https://www.cs.cmu.edu/~pattis/15-1XX/common/handouts/ascii.html
			// https://www.json.org/json-en.html
			if        (s[i] ==  8) { // backspace
				ss << "\\b"; 
			} else if (s[i] ==  9) { // tab
				ss << "\\t";
			} else if (s[i] == 10) { // linefeed
				ss << "\\n";
			} else if (s[i] == 12) { // form feed
				ss << "\\f";
			} else if (s[i] == 13) { // carriage return
				ss << "\\r";
			} else {
				ss << s[i];
			}
		} else if (s[i] >= 32 && s[i] <= 127) {
			//cout << "std ascii" << endl;
			if (s[i] == '"' || s[i] == '\\' || s[i] == '/') {
				ss << "\\" << s[i];
			} else {
				ss << s[i];
			}
		} else if ( ((s[i  ] & 0b11011111) >> 5) == 0b110 && 
			    i + 1 < len &&
			    ((s[i+1] & 0b10111111) >> 6) == 0b10
				) {
			// 2 byte unicode character
			cout << "2 byte unicode" << endl;
			ss << s[i] << s[i+1];
			i+=1;
		} else if ( ((s[i  ] & 0b11101111) >> 4) == 0b1110 && 
			    i + 2 < len &&
			    ((s[i+1] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+2] & 0b10111111) >> 6) == 0b10 
				) {
			// 3 byte unicode character
			cout << "3 byte unicode" << endl;
			ss << s[i] << s[i+1] << s[i+2];
			i+=2;
		} else if ( ((s[i  ] & 0b11110111) >> 3) == 0b11110 && 
			    i + 3 < len &&
			    ((s[i+1] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+2] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+3] & 0b10111111) >> 6) == 0b10 
				) {
			cout << "4 byte unicode" << endl;
			ss << s[i] << s[i+1] << s[i+2] << s[i+3];
			i+=3;
		} else if ( ((s[i  ] & 0b11111011) >> 2) == 0b111110 && 
			    i + 4 < len &&
			    ((s[i+1] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+2] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+3] & 0b10111111) >> 6) == 0b10 &&
			    ((s[i+4] & 0b10111111) >> 6) == 0b10 
				) {
			cout << "5 byte unicode" << endl;
			ss << s[i] << s[i+1] << s[i+2] << s[i+3];
			i+=4;
		} else {
			// 4 byte unicode character
			cout << "else clause char: " << (std::bitset<8>(s[i])) << endl;
			ss << s[i] << s[i+1] << s[i+2] << s[i+3];
			i+=3;
		}
	}
	return ss.str();
}

